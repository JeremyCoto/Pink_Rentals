        P_ESTADOS_ID_ESTADO_PK IN G2_SC508_VT_PROYECTO.FIDE_ESTADOS_TB.ESTADOS_ID_ESTADO_PK%TYPE
    ) RETURN VARCHAR2 IS
        V_ESTADO VARCHAR2(200);
    BEGIN
        SELECT estado
        INTO V_ESTADO
        FROM G2_SC508_VT_PROYECTO.FIDE_ESTADOS_TB
        WHERE ESTADOS_ID_ESTADO_PK = P_ESTADOS_ID_ESTADO_PK;
        DBMS_OUTPUT.PUT_LINE('EXITO [FIDE_ESTADO_NOMBRE_FN]: Estado = ' || V_ESTADO);
        RETURN V_ESTADO;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            DBMS_OUTPUT.PUT_LINE('ADVERTENCIA [FIDE_ESTADO_NOMBRE_FN]: Estado ' || P_ESTADOS_ID_ESTADO_PK || ' no encontrado.');
            RETURN NULL;
    END FIDE_ESTADO_NOMBRE_FN;    
    
    -----------------------------------------------------------------
    -- 3. FIDE_TOTAL_RESERVACION_FN (Funciones Apoyo / Reservaciones)
    -----------------------------------------------------------------
    FUNCTION FIDE_TOTAL_RESERVACION_FN(
        P_RESERVACIONES_ID_RESERVACION_PK IN G2_SC508_VT_PROYECTO.FIDE_RESERVACIONES_TB.RESERVACIONES_ID_RESERVACION_PK%TYPE
    ) RETURN NUMBER IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT NVL(SUM(cantidad * precio_unitario), 0)
        INTO V_TOTAL
        FROM G2_SC508_VT_PROYECTO.FIDE_DETALLE_RESERVACION_TB
        WHERE RESERVACIONES_ID_RESERVACION_PK = P_RESERVACIONES_ID_RESERVACION_PK;
        DBMS_OUTPUT.PUT_LINE('EXITO [FIDE_TOTAL_RESERVACION_FN]: Total reservación ' || P_RESERVACIONES_ID_RESERVACION_PK || ' = ' || V_TOTAL);
        RETURN V_TOTAL;
    END FIDE_TOTAL_RESERVACION_FN;
    
    ---------------------------------------------------------------------
    -- 4. FIDE_CALCULAR_IMPUESTO_FN (Funciones Apoyo / Cálculo Impuestos)
    ---------------------------------------------------------------------
    FUNCTION FIDE_CALCULAR_IMPUESTO_FN(
        P_MONTO_BASE IN NUMBER
    ) RETURN NUMBER IS
        V_IMPUESTO NUMBER;
    BEGIN
        V_IMPUESTO := ROUND(P_MONTO_BASE * 0.13, 2);
        DBMS_OUTPUT.PUT_LINE('EXITO [FIDE_CALCULAR_IMPUESTO_FN]: Impuesto calculado = ' || V_IMPUESTO);
        RETURN V_IMPUESTO;
    END FIDE_CALCULAR_IMPUESTO_FN;    
    
    -----------------------------------------------------------------------
    -- 5. FIDE_CONTAR_RESERVACIONES_CLIENTE_FN (Funciones Apoyo / Reportes)
    -----------------------------------------------------------------------
    FUNCTION FIDE_CONTAR_RESERVACIONES_CLIENTE_FN(
        P_USUARIOS_ID_CEDULA_PK IN G2_SC508_VT_PROYECTO.FIDE_RESERVACIONES_TB.USUARIOS_ID_CEDULA_PK%TYPE,
        P_FECHA_INICIO IN DATE,
        P_FECHA_FIN    IN DATE
    ) RETURN NUMBER IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_TOTAL
        FROM G2_SC508_VT_PROYECTO.FIDE_RESERVACIONES_TB
        WHERE USUARIOS_ID_CEDULA_PK = P_USUARIOS_ID_CEDULA_PK
        AND fecha_reservacion BETWEEN P_FECHA_INICIO AND P_FECHA_FIN;
        DBMS_OUTPUT.PUT_LINE('EXITO [FIDE_CONTAR_RESERVACIONES_CLIENTE_FN]: Cliente ' || P_USUARIOS_ID_CEDULA_PK ||
                              ' tiene ' || V_TOTAL || ' reservaciones.');
        RETURN V_TOTAL;
    END FIDE_CONTAR_RESERVACIONES_CLIENTE_FN;
        
    ------------------------------------------------------------------------
    -- 6. FIDE_DISPONIBILIDAD_SERVICIO_FN (Funciones Apoyo / Disponibilidad)
    ------------------------------------------------------------------------
    FUNCTION FIDE_DISPONIBILIDAD_SERVICIO_FN(
        P_SERVICIOS_ID_SERVICIO_PK IN G2_SC508_VT_PROYECTO.FIDE_SERVICIOS_TB.SERVICIOS_ID_SERVICIO_PK%TYPE,
        P_FECHA_RESERVACION IN DATE,
        P_HORA_INICIO IN TIMESTAMP,
        P_HORA_FIN    IN TIMESTAMP
    ) RETURN NUMBER IS
        V_OCUPADAS NUMBER;
    BEGIN
        SELECT COUNT(DISTINCT R.RESERVACIONES_ID_RESERVACION_PK)
        INTO V_OCUPADAS
        FROM G2_SC508_VT_PROYECTO.FIDE_RESERVACIONES_TB R
        JOIN G2_SC508_VT_PROYECTO.FIDE_DETALLE_RESERVACION_TB DR
            ON DR.RESERVACIONES_ID_RESERVACION_PK = R.RESERVACIONES_ID_RESERVACION_PK
        WHERE DR.SERVICIOS_ID_SERVICIO_PK = P_SERVICIOS_ID_SERVICIO_PK
        AND R.fecha_reservacion = P_FECHA_RESERVACION
        AND R.hora_inicio < P_HORA_FIN 
        AND R.hora_fin    > P_HORA_INICIO;
        DBMS_OUTPUT.PUT_LINE('EXITO [FIDE_DISPONIBILIDAD_SERVICIO_FN]: Reservas que traslapan = ' || V_OCUPADAS);
        RETURN V_OCUPADAS;
    END FIDE_DISPONIBILIDAD_SERVICIO_FN;
    
