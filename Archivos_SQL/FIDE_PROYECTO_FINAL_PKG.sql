        RETURN V_TOTAL;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            DBMS_OUTPUT.PUT_LINE('ADVERTENCIA [FIDE_TOTAL_FACTURA_FN]: Factura ' || P_FACTURACION_ID_NUMERO_FACTURA_PK || ' no existe.');
            RETURN 0;
    END FIDE_TOTAL_FACTURA_FN;

